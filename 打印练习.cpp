#include <stdio.h>

int main()
{
	printf("打印随机字符：%c %c %c %c %c\n",170,50,150,175,157);
	printf("前边用空格隔开：%10d\n",2015);
	printf("同上;%14d\n",2015);
	printf("同上但改为0：%010d\n",2015);
	printf("右对齐，保留两位：%10.2f\n",3.1415926);
	printf("左对齐，保留三位：%-10.3f\n",3.1415926);
	printf("右对齐指数形式：%10e\n",520000.0);
	printf("右对齐指数形式：%10E\n",520000.0);
	printf("左对齐指数形式：%10e\n",520000.0);

	
	return 0;
	
}
